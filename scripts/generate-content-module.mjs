import { readFile, readdir, writeFile } from "node:fs/promises";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const projectRoot = dirname(dirname(fileURLToPath(import.meta.url)));

async function collect(directory) {
  const contentDirectory = join(projectRoot, "content", directory);
  const files = (await readdir(contentDirectory))
    .filter((file) => file.endsWith(".mdx"))
    .sort();

  return Object.fromEntries(
    await Promise.all(
      files.map(async (file) => [
        `content/${directory}/${file}`,
        await readFile(join(contentDirectory, file), "utf8"),
      ]),
    ),
  );
}

const [projectFiles, experienceFiles] = await Promise.all([
  collect("projects"),
  collect("experience"),
]);

const output = `// Generated by scripts/generate-content-module.mjs. Do not edit directly.\n\nexport const projectFiles = ${JSON.stringify(projectFiles, null, 2)} as const;\n\nexport const experienceFiles = ${JSON.stringify(experienceFiles, null, 2)} as const;\n`;
const outputPath = join(projectRoot, "content", "generated.ts");

let current = null;
try {
  current = await readFile(outputPath, "utf8");
} catch (error) {
  if (error?.code !== "ENOENT") throw error;
}

if (current !== output) {
  await writeFile(outputPath, output);
  console.log("Generated bundled portfolio content.");
}
